import streamlit as st

st.title("Proyecto: Evento Nacional de Conteo y Depósito de Monedas para el Ahorro Voluntario en las AFOREs")

st.header("1. Título del Proyecto")
st.subheader("Evento Nacional de Conteo y Depósito de Monedas para Fomentar el Ahorro Voluntario en las 10 AFOREs de México")

st.header("2. Descripción del Proyecto")
st.write("""
El proyecto consiste en organizar eventos periódicos de conteo y depósito de monedas en las 32 entidades federativas de México, utilizando los Centros de Atención al Público (CAP) como puntos de encuentro. Estos eventos están diseñados para fomentar el ahorro voluntario en las 10 AFOREs que existen en México, mediante actividades festivas, educativas y recreativas que mantengan a los empleados motivados y comprometidos con sus metas de ahorro.
""")

st.header("3. Objetivos del Proyecto")
st.write("""
• Objetivo General: Incrementar el ahorro voluntario de los trabajadores afiliados a las 10 AFOREs mediante eventos periódicos de conteo y depósito de monedas, promoviendo una cultura de ahorro y educación financiera.

• Objetivos Específicos:
  - Implementar eventos de conteo y depósito de monedas en los CAPs de las 32 entidades federativas.
  - Fomentar la participación de los empleados mediante actividades festivas y recreativas.
  - Proporcionar educación financiera a los empleados durante los eventos.
  - Registrar y depositar las cantidades ahorradas en las cuentas de ahorro voluntario en las 10 AFOREs de manera inmediata.
  - Crear un ambiente positivo y motivador que refuerce la cultura de ahorro entre los empleados.
""")

st.header("4. Alcance del Proyecto")
st.write("""
El proyecto abarcará todas las 32 entidades federativas de México, utilizando los CAPs como puntos centrales para la realización de los eventos. Incluirá la participación de empleados afiliados a cualquiera de las 10 AFOREs y se llevará a cabo de manera periódica (mensual o trimestral).
""")

st.header("5. Planificación y Coordinación")
st.write("""
• Equipo Nacional:
  - Responsable de la planificación general, comunicación y supervisión del proyecto.
  - Coordinación con directores regionales y representantes de cada CAP.

• Directores Regionales:
  - Coordinarán los eventos en sus respectivas entidades federativas.
  - Asegurarán la implementación adecuada de las actividades y supervisarán el conteo de monedas.

• Colaboración con las AFOREs:
  - Establecer un comité de coordinación con representantes de las 10 AFOREs para alinear objetivos y estrategias.
  - Garantizar la participación activa y el apoyo logístico de las AFOREs.
""")

st.header("6. Logística y Recursos")
st.write("""
• Selección de Ubicaciones:
  - Utilizar los CAPs existentes como puntos de encuentro.
  - Alternativas de espacios en caso de necesidad (salas de conferencias, auditorios locales, etc.).

• Infraestructura:
  - Máquinas contadoras de monedas.
  - Mesas de conteo y personal capacitado.
  - Sistemas digitales para el registro y depósito de ahorros.
""")

st.header("7. Actividades del Evento")
st.write("""
• Recepción y Registro:
  - Bienvenida festiva con decoraciones, música y refrigerios.
  - Registro de participantes mediante un sistema digital.

• Conteo de Monedas:
  - Estaciones de conteo con máquinas contadoras.
  - Supervisión para garantizar transparencia y precisión.

• Charlas y Talleres:
  - Educación financiera y planificación de ahorros.
  - Expertos financieros y representantes de las 10 AFOREs como ponentes.

• Actividades Recreativas:
  - Juegos y competencias relacionadas con finanzas.
  - Rifas y sorteos de premios para incentivar la participación.
""")

st.header("8. Promoción y Comunicación")
st.write("""
• Campaña Nacional:
  - Informar a los empleados sobre los eventos a través de correos electrónicos, intranet, redes sociales y posters.
  - Publicación de un calendario nacional con las fechas de los eventos.

• Incentivos:
  - Ofrecer premios y reconocimientos a los empleados que participen activamente.
  - Organizar competencias amistosas entre departamentos o equipos.
""")

st.header("9. Seguimiento y Evaluación")
st.write("""
• Monitoreo:
  - Solicitar reportes diarios de cada CAP durante el evento.
  - Evaluación post-evento para identificar áreas de mejora.

• Comunicación de Resultados:
  - Publicación de un informe nacional que resuma los resultados del evento.
  - Agradecimientos a todos los participantes y organizadores.
""")

st.header("10. Ejemplo de Itinerario del Evento en los CAPs")
st.write("""
1. 09:00 - 09:30: Recepción y registro de participantes.
2. 09:30 - 10:30: Inicio del conteo de monedas en estaciones designadas.
3. 10:30 - 11:00: Charlas sobre educación financiera y ahorro.
4. 11:00 - 12:00: Continuación del conteo y actividades recreativas.
5. 12:00 - 12:30: Juegos y competencias amigables.
6. 12:30 - 13:00: Rifa de premios y reconocimiento a los participantes.
""")

st.header("11. Presupuesto Estimado")
st.write("""
• Materiales y Equipos:
  - Alcancías y contenedores personalizados.
  - Máquinas contadoras de monedas.
  - Decoraciones y materiales de promoción.

• Logística y Operación:
  - Transporte y logística para la distribución de materiales.
  - Personal capacitado y supervisores.
  - Sistemas digitales para el registro de ahorros.

• Incentivos y Premios:
  - Premios y reconocimientos para los participantes.
  - Costos asociados a rifas y sorteos.
""")

st.header("12. Beneficios Esperados")
st.write("""
• Cultura de Ahorro:
  - Fomentar una cultura de ahorro voluntario entre los empleados afiliados a las 10 AFOREs.

• Educación Financiera:
  - Mejorar el conocimiento y habilidades financieras de los empleados.

• Motivación y Compromiso:
  - Mantener a los empleados motivados y comprometidos con sus metas de ahorro.

• Impacto Positivo:
  - Crear un ambiente positivo y motivador dentro de la empresa y entre los empleados.
""")

st.header("Implementación para las 32 Entidades Federativas")
st.write("""
1. Coordinación con las AFOREs:
  - Comité de Coordinación: Formar un comité con representantes de las 10 AFOREs para asegurar una alineación de objetivos y estrategias.
  - Directores Regionales de AFOREs: Cada AFORE asignará un director regional para coordinar los eventos en cada entidad federativa.
2. Eventos Locales:
  - Distribución de Alcancías: Cada AFORE distribuirá alcancías personalizadas a sus afiliados en cada entidad federativa.
  - Centros de Atención: Los CAPs servirán como puntos de encuentro para los eventos, organizados por las AFOREs en colaboración con los CAPs locales.
3. Participación de los Afiliados:
  - Convocatoria y Registro: Las AFOREs enviarán invitaciones a sus afiliados y registrarán su participación mediante una plataforma digital.
  - Actividades Educativas: Las AFOREs organizarán talleres y charlas sobre educación financiera durante los eventos.
4. Conteo y Depósito:
  - Sistema Uniforme: Implementar un sistema uniforme de conteo y registro de ahorros en todas las entidades federativas.
  - Depósito Inmediato: Asegurar que las cantidades ahorradas se depositen en las cuentas de ahorro voluntario de los afiliados de manera inmediata.
""")

st.header("Beneficios Adicionales de la Implementación a Nivel Nacional")
st.write("""
1. Ampliación del Impacto: Fomenta una cultura de ahorro en todo el país, alcanzando a trabajadores de diversas regiones.
2. Apoyo de las AFOREs: La participación de las 10 AFOREs garantiza un esfuerzo conjunto y coordinado, maximizando los recursos y el alcance del proyecto.
3. Educación y Motivación: Proporciona educación financiera a un amplio grupo de personas, mejorando sus conocimientos y habilidades en la gestión de sus finanzas.
4. Impacto Duradero: Implementar este proyecto a nivel nacional con la participación de las 10 AFOREs no solo incrementará el ahorro voluntario, sino que también fortalecerá la educación financiera y la cultura de ahorro entre los trabajadores, generando un impacto duradero y positivo en su bienestar financiero.
""")


